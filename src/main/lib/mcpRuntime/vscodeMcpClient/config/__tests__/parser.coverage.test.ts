// ── formatToVSCodeSettings multiple servers ───────────────────────────────────

describe('formatToVSCodeSettings — multiple servers', () => {
  it('formats multiple servers', () => {
    const configs = [
      { name: 'a', transport: 'stdio' as const, command: 'n', args: ['x'] },
      { name: 'b', transport: 'StreamableHttp' as const, url: 'http://y' },
    ];
    const parsed = JSON.parse(formatToVSCodeSettings(configs));
    expect(Object.keys(parsed.mcp.servers)).toHaveLength(2);
    expect(parsed.mcp.servers.a.type).toBe('stdio');
    expect(parsed.mcp.servers.b.type).toBe('http');
  });

  it('stdio with empty args does not include args', () => {
    const configs = [{ name: 's', transport: 'stdio' as const, command: 'x', args: [] }];
    const parsed = JSON.parse(formatToVSCodeSettings(configs));
    expect(parsed.mcp.servers.s.args).toBeUndefined();
  });

  it('sse transport sets type=sse', () => {
    const configs = [{ name: 's', transport: 'sse' as const, url: 'http://x/sse' }];
    const parsed = JSON.parse(formatToVSCodeSettings(configs));
    expect(parsed.mcp.servers.s.type).toBe('sse');
  });
});

