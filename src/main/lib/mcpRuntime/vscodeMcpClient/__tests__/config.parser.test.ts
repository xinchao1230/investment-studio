describe('formatToStandardJson', () => {
  it('formats stdio config correctly', () => {
    const parsedConfig = {
      serverName: 'test',
      transportType: 'stdio' as const,
      config: { command: 'node', args: ['s.js'] },
      isAutoGenerated: false,
      detectedFormat: 'Format 1'
    };
    const json = formatToStandardJson(parsedConfig);
    const parsed = JSON.parse(json);
    expect(parsed.command).toBe('node');
    expect(parsed.args).toEqual(['s.js']);
  });

  it('formats http config correctly', () => {
    const parsedConfig = {
      serverName: 'test',
      transportType: 'StreamableHttp' as const,
      config: { url: 'http://host/mcp' },
      isAutoGenerated: false,
      detectedFormat: 'Format 2'
    };
    const json = formatToStandardJson(parsedConfig);
    const parsed = JSON.parse(json);
    expect(parsed.url).toBe('http://host/mcp');
    expect(parsed.env).toBeUndefined();
  });

  it('omits env when empty', () => {
    const parsedConfig = {
      serverName: 'test',
      transportType: 'stdio' as const,
      config: { command: 'node', args: [], env: {} },
      isAutoGenerated: false,
      detectedFormat: ''
    };
    const json = formatToStandardJson(parsedConfig);
    const parsed = JSON.parse(json);
    expect(parsed.env).toBeUndefined();
  });
});

