/**
 * Tests for config/parser.ts
 */

import { describe, it, expect } from 'vitest';
import {
  parseMcpConfig,
  parseVSCodeConfigToInternal,
  formatToStandardJson,
  formatToMcpServersWrapper,
  formatToVSCodeSettings,
  formatToVSCodeMcpJson,
  isExampleConfiguration
} from '../config/parser';

// ---------------------------------------------------------------------------
// parseMcpConfig
// ---------------------------------------------------------------------------

describe('parseMcpConfig', () => {
  it('returns error on empty input', () => {
    const result = parseMcpConfig('');
    expect(result.success).toBe(false);
    expect(result.error).toContain('empty');
  });

  it('returns error on whitespace-only input', () => {
    const result = parseMcpConfig('   ');
    expect(result.success).toBe(false);
  });

  it('returns error on invalid JSON', () => {
    const result = parseMcpConfig('{ bad json }');
    expect(result.success).toBe(false);
    expect(result.error).toContain('Invalid JSON');
  });

  it('parses Format 1 – stdio basic format', () => {
    const input = JSON.stringify({ command: 'python', args: ['-m', 'server'] });
    const result = parseMcpConfig(input);
    expect(result.success).toBe(true);
    expect(result.data!.transportType).toBe('stdio');
    expect(result.data!.config.command).toBe('python');
    expect(result.data!.config.args).toEqual(['-m', 'server']);
  });

  it('parses Format 2 – SSE/HTTP basic format', () => {
    const input = JSON.stringify({ url: 'http://localhost:8000/sse' });
    const result = parseMcpConfig(input);
    expect(result.success).toBe(true);
    expect(result.data!.transportType).toBe('sse');
    expect(result.data!.config.url).toBe('http://localhost:8000/sse');
  });

  it('parses HTTP URL without sse as StreamableHttp', () => {
    const input = JSON.stringify({ url: 'http://localhost:8000/mcp' });
    const result = parseMcpConfig(input);
    expect(result.success).toBe(true);
    expect(result.data!.transportType).toBe('StreamableHttp');
  });

  it('parses Format 3 – mcpServers wrapper with type=http', () => {
    const input = JSON.stringify({
      mcpServers: {
        myServer: { url: 'http://localhost:3000', type: 'http' }
      }
    });
    const result = parseMcpConfig(input);
    expect(result.success).toBe(true);
    expect(result.data!.serverName).toBe('myServer');
    expect(result.data!.transportType).toBe('StreamableHttp');
  });

  it('parses Format 4 – mcpServers wrapper with type=stdio', () => {
    const input = JSON.stringify({
      mcpServers: {
        myServer: { command: 'node', args: ['server.js'], type: 'stdio' }
      }
    });
    const result = parseMcpConfig(input);
    expect(result.success).toBe(true);
    expect(result.data!.serverName).toBe('myServer');
    expect(result.data!.transportType).toBe('stdio');
  });

  it('parses Format 5 – server fragment with type=http', () => {
    const input = JSON.stringify({
      myServer: { url: 'http://localhost:3000', type: 'http' }
    });
    const result = parseMcpConfig(input);
    expect(result.success).toBe(true);
    expect(result.data!.serverName).toBe('myServer');
    expect(result.data!.transportType).toBe('StreamableHttp');
  });

  it('parses Format 6 – server fragment with type=stdio', () => {
    const input = JSON.stringify({
      myServer: { command: 'npx', args: ['server'], type: 'stdio' }
    });
    const result = parseMcpConfig(input);
    expect(result.success).toBe(true);
    expect(result.data!.serverName).toBe('myServer');
    expect(result.data!.transportType).toBe('stdio');
  });

  it('respects currentTransportType hint when no type field', () => {
    const input = JSON.stringify({ url: 'http://localhost:3000' });
    const result = parseMcpConfig(input, 'sse');
    expect(result.success).toBe(true);
    expect(result.data!.transportType).toBe('sse');
  });

  it('includes env when present', () => {
    const input = JSON.stringify({
      command: 'node',
      args: ['srv.js'],
      env: { API_KEY: 'secret' }
    });
    const result = parseMcpConfig(input);
    expect(result.success).toBe(true);
    expect(result.data!.config.env).toEqual({ API_KEY: 'secret' });
  });

  it('auto-generates server name when not provided', () => {
    const input = JSON.stringify({ command: 'python', args: [] });
    const result = parseMcpConfig(input);
    expect(result.success).toBe(true);
    expect(result.data!.isAutoGenerated).toBe(true);
    expect(result.data!.serverName).toMatch(/^mcp-server-/);
  });

  it('strips invisible Unicode characters before parsing', () => {
    // Wrap valid JSON with BOM and NBSP
    const input = '﻿{"command":"node","args":[]} ';
    const result = parseMcpConfig(input);
    expect(result.success).toBe(true);
  });

  it('handles type=sse field correctly', () => {
    const input = JSON.stringify({
      mcpServers: { s: { url: 'http://host/sse', type: 'sse' } }
    });
    const result = parseMcpConfig(input);
    expect(result.data!.transportType).toBe('sse');
  });

  it('handles type=streamable-http field correctly', () => {
    const input = JSON.stringify({
      mcpServers: { s: { url: 'http://host/mcp', type: 'streamable-http' } }
    });
    const result = parseMcpConfig(input);
    expect(result.data!.transportType).toBe('StreamableHttp');
  });
});

