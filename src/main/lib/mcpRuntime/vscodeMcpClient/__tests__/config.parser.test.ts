describe('formatToMcpServersWrapper', () => {
  it('wraps stdio config in mcpServers', () => {
    const parsedConfig = {
      serverName: 'myServer',
      transportType: 'stdio' as const,
      config: { command: 'python', args: ['-m', 'srv'] },
      isAutoGenerated: false,
      detectedFormat: ''
    };
    const json = formatToMcpServersWrapper(parsedConfig);
    const parsed = JSON.parse(json);
    expect(parsed.mcpServers.myServer.command).toBe('python');
    expect(parsed.mcpServers.myServer.type).toBe('stdio');
  });

  it('wraps HTTP config in mcpServers', () => {
    const parsedConfig = {
      serverName: 'httpServer',
      transportType: 'StreamableHttp' as const,
      config: { url: 'http://host/mcp' },
      isAutoGenerated: false,
      detectedFormat: ''
    };
    const json = formatToMcpServersWrapper(parsedConfig);
    const parsed = JSON.parse(json);
    expect(parsed.mcpServers.httpServer.url).toBe('http://host/mcp');
    expect(parsed.mcpServers.httpServer.type).toBe('StreamableHttp');
  });
});

