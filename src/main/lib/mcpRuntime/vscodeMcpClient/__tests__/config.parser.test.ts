describe('formatToVSCodeSettings', () => {
  it('produces settings.json with mcp.servers', () => {
    const configs = [
      { name: 'srv', transport: 'stdio' as const, command: 'node', args: ['s.js'] }
    ];
    const json = formatToVSCodeSettings(configs);
    const parsed = JSON.parse(json);
    expect(parsed.mcp.servers.srv.command).toBe('node');
    expect(parsed.mcp.servers.srv.type).toBe('stdio');
  });

  it('handles SSE transport', () => {
    const configs = [
      { name: 'sse-srv', transport: 'sse' as const, url: 'http://host/sse' }
    ];
    const json = formatToVSCodeSettings(configs);
    const parsed = JSON.parse(json);
    expect(parsed.mcp.servers['sse-srv'].type).toBe('sse');
    expect(parsed.mcp.servers['sse-srv'].url).toBe('http://host/sse');
  });

  it('handles StreamableHttp transport', () => {
    const configs = [
      { name: 'http-srv', transport: 'StreamableHttp' as const, url: 'http://host/mcp' }
    ];
    const json = formatToVSCodeSettings(configs);
    const parsed = JSON.parse(json);
    expect(parsed.mcp.servers['http-srv'].type).toBe('http');
  });
});

describe('formatToVSCodeMcpJson', () => {
  it('produces mcp.json with servers and inputs', () => {
    const configs = [
      { name: 'srv', transport: 'stdio' as const, command: 'python', args: ['-m', 'srv'] }
    ];
    const json = formatToVSCodeMcpJson(configs);
    const parsed = JSON.parse(json);
    expect(parsed.servers.srv.command).toBe('python');
    expect(parsed.inputs).toEqual([]);
  });
});

