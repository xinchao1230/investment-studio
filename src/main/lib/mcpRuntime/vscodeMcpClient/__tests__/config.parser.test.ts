describe('parseVSCodeConfigToInternal', () => {
  it('returns error on empty input', () => {
    const result = parseVSCodeConfigToInternal('', 'mcp.json');
    expect(result.success).toBe(false);
  });

  it('parses mcp.json format with stdio server', () => {
    const input = JSON.stringify({
      servers: {
        myServer: { type: 'stdio', command: 'node', args: ['s.js'] }
      }
    });
    const result = parseVSCodeConfigToInternal(input, 'mcp.json');
    expect(result.success).toBe(true);
    expect(result.data!.serverName).toBe('myServer');
    expect(result.data!.transportType).toBe('stdio');
    expect(result.data!.config.command).toBe('node');
  });

  it('parses settings.json format with http server', () => {
    const input = JSON.stringify({
      mcp: {
        servers: {
          remote: { type: 'http', url: 'http://localhost:3000' }
        }
      }
    });
    const result = parseVSCodeConfigToInternal(input, 'settings.json');
    expect(result.success).toBe(true);
    expect(result.data!.serverName).toBe('remote');
    expect(result.data!.transportType).toBe('StreamableHttp');
  });

  it('returns error when mcp.json has no servers section', () => {
    const input = JSON.stringify({ foo: 'bar' });
    const result = parseVSCodeConfigToInternal(input, 'mcp.json');
    expect(result.success).toBe(false);
    expect(result.error).toContain('No MCP server found');
  });

  it('returns error when settings.json has no mcp.servers section', () => {
    const input = JSON.stringify({ mcp: {} });
    const result = parseVSCodeConfigToInternal(input, 'settings.json');
    expect(result.success).toBe(false);
  });

  it('returns error when servers section is empty', () => {
    const input = JSON.stringify({ servers: {} });
    const result = parseVSCodeConfigToInternal(input, 'mcp.json');
    expect(result.success).toBe(false);
  });

  it('returns error on invalid JSON', () => {
    const result = parseVSCodeConfigToInternal('{ invalid', 'mcp.json');
    expect(result.success).toBe(false);
    expect(result.error).toContain('Invalid JSON');
  });

  it('includes env when present', () => {
    const input = JSON.stringify({
      servers: {
        s: { type: 'stdio', command: 'node', env: { TOKEN: 'abc' } }
      }
    });
    const result = parseVSCodeConfigToInternal(input, 'mcp.json');
    expect(result.success).toBe(true);
    expect(result.data!.config.env).toEqual({ TOKEN: 'abc' });
  });

  it('detects SSE from URL ending with /sse when type=http', () => {
    const input = JSON.stringify({
      servers: { s: { type: 'http', url: 'http://host/mcp/sse' } }
    });
    const result = parseVSCodeConfigToInternal(input, 'mcp.json');
    expect(result.success).toBe(true);
    expect(result.data!.transportType).toBe('sse');
  });
});

