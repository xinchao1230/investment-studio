export function renderSprite(bones: CompanionBones, frame: number): string[] {
  const speciesFrames = SPRITE_DATA[bones.species];
  const safeFrame = frame % speciesFrames.length;
  const lines = [...speciesFrames[safeFrame]];
  if (bones.hat !== 'none') {
    lines[0] = HAT_OVERLAYS[bones.hat];
  }
  for (let i = 0; i < lines.length; i++) {
    lines[i] = lines[i].replace(/\{E\}/g, bones.eye);
  }
  return lines;
}

export function renderFace(bones: CompanionBones): string {
  return `(${bones.eye}${bones.eye})`;
}

export function spriteFrameCount(species: Species): number {
  return SPRITE_DATA[species].length;
}
