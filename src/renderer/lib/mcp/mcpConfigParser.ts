/**
 * Parse VSCode format and convert to internal format compatible with existing parser
 */
export function parseVSCodeConfigToInternal(
  input: string,
  format: 'settings.json' | 'mcp.json'
): McpConfigParseResult {
  try {
    if (!input.trim()) {
      return {
        success: false,
        error: 'Configuration cannot be empty'
      }
    }

    // Clean invisible characters
    const cleanedInput = cleanInvisibleCharacters(input.trim())
    
    // Try to parse JSON
    let parsedConfig: any
    try {
      parsedConfig = JSON.parse(cleanedInput)
    } catch (e) {
      return {
        success: false,
        error: `Invalid JSON format: ${e instanceof Error ? e.message : 'Unknown error'}`
      }
    }

    // Extract servers based on format
    let servers: Record<string, any> = {}
    
    if (format === 'settings.json' && parsedConfig.mcp?.servers) {
      servers = parsedConfig.mcp.servers
    } else if (format === 'mcp.json' && parsedConfig.servers) {
      servers = parsedConfig.servers
    } else {
      return {
        success: false,
        error: 'No MCP servers found in configuration'
      }
    }

    // If multiple servers, return the first one for compatibility with existing parser
    const serverNames = Object.keys(servers)
    if (serverNames.length === 0) {
      return {
        success: false,
        error: 'No MCP servers found in configuration'
      }
    }

    const firstServerName = serverNames[0]
    const firstServerConfig = servers[firstServerName]

    // Convert to internal format
    const transportType = convertVSCodeTransportType(firstServerConfig)
    
    const config: ParsedMcpConfig['config'] = {}
    
    if (transportType === 'stdio') {
      config.command = firstServerConfig.command
      config.args = firstServerConfig.args
      if (firstServerConfig.env && Object.keys(firstServerConfig.env).length > 0) {
        config.env = firstServerConfig.env
      }
    } else {
      config.url = firstServerConfig.url
      if (firstServerConfig.env && Object.keys(firstServerConfig.env).length > 0) {
        config.env = firstServerConfig.env
      }
    }

    return {
      success: true,
      data: {
        serverName: firstServerName,
        transportType,
        config,
        isAutoGenerated: false,
        detectedFormat: `VSCode ${format} format`
      }
    }

  } catch (error) {
    return {
      success: false,
      error: `Parsing error: ${error instanceof Error ? error.message : 'Unknown error'}`
    }
  }
}

/**
 * Convert VSCode transport type to internal transport type
 */
function convertVSCodeTransportType(vscodeConfig: any): 'stdio' | 'sse' | 'StreamableHttp' {
  if (vscodeConfig.type) {
    switch (vscodeConfig.type.toLowerCase()) {
      case 'stdio':
        return 'stdio'
      case 'sse':
        return 'sse'
      case 'http':
      case 'streamablehttp':
        // Determine based on URL if available
        if (vscodeConfig.url && vscodeConfig.url.endsWith('/sse')) {
          return 'sse'
        }
        return 'StreamableHttp'
      default:
        // Fall through to auto-detection
        break
    }
  }

  // Auto-detect based on available fields
  if (vscodeConfig.command || vscodeConfig.args) {
    return 'stdio'
  }

  if (vscodeConfig.url) {
    // Determine HTTP transport type based on URL
    return vscodeConfig.url.endsWith('/sse') ? 'sse' : 'StreamableHttp'
  }

  // Default fallback
  return 'stdio'
}

/**
 * Validate VSCode configuration format
 */
export function validateVSCodeConfig(input: string, format: 'settings.json' | 'mcp.json'): {
  isValid: boolean
  errors: string[]
  serverCount: number
} {
  const errors: string[] = []
  let serverCount = 0

  try {
    const parsedConfig = JSON.parse(input)
    
    let servers: Record<string, any> = {}
    
    if (format === 'settings.json') {
      if (!parsedConfig.mcp) {
        errors.push('Missing "mcp" section in settings.json')
        return { isValid: false, errors, serverCount: 0 }
      }
      if (!parsedConfig.mcp.servers) {
        errors.push('Missing "mcp.servers" section in settings.json')
        return { isValid: false, errors, serverCount: 0 }
      }
      servers = parsedConfig.mcp.servers
    } else if (format === 'mcp.json') {
      if (!parsedConfig.servers) {
        errors.push('Missing "servers" section in mcp.json')
        return { isValid: false, errors, serverCount: 0 }
      }
      servers = parsedConfig.servers
    }

    // Validate each server configuration
    for (const [serverName, serverConfig] of Object.entries(servers)) {
      if (!serverConfig || typeof serverConfig !== 'object') {
        errors.push(`Invalid server configuration for "${serverName}"`)
        continue
      }

      const config = serverConfig as any
      
      // Check if it has either command/args (stdio) or url (http/sse)
      const hasStdioConfig = config.command || config.args
      const hasHttpConfig = config.url
      
      if (!hasStdioConfig && !hasHttpConfig) {
        errors.push(`Server "${serverName}" missing required configuration (command/args or url)`)
        continue
      }

      // Count valid servers
      if (!config.disabled) {
        serverCount++
      }
    }

    return {
      isValid: errors.length === 0,
      errors,
      serverCount
    }
  } catch (parseError) {
    errors.push(`Invalid JSON format: ${parseError instanceof Error ? parseError.message : 'Parse error'}`)
    return { isValid: false, errors, serverCount: 0 }
  }
}